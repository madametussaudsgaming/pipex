#include "get_next_line.h"

static char	*read_line(int fd, char *line)
{
	char	*buf;
	ssize_t	read_byte;

	buf = malloc(sizeof(char) * (BUFFER_SIZE + 1));
	if (!buf)
		return (NULL);
	read_byte = 1;
	while (read_byte > 0 && !gnl_strchr(line, '\n'))
	{
		read_byte = read(fd, buf, BUFFER_SIZE);
		if (read_byte == -1)
		{
			free(buf);
			return (NULL);
		}
		buf[read_byte] = '\0';
		line = gnl_strjoin(line, buf);
	}
	free(buf);
	return (line);
}

static char	*read_next(char *line)
{
	char	*buffer;
	int		i;
	int		j;

	i = 0;
	while (line[i] && line[i] != '\n')
		i++;
	if (line[i] == '\0')
	{
		free(line);
		return (NULL);
	}
	i++;
	buffer = malloc(sizeof(char) * (gnl_strlen(line) - i + 1));
	if (!buffer)
		return (NULL);
	j = 0;
	while (line[i])
		buffer[j++] = line[i++];
	buffer[j] = '\0';
	free(line);
	return (buffer);
}

static char	*extract(char *line)
{
	char	*next_line;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (!line[i])
		return (NULL);
	while (line[i] != '\n' && line[i])
		i++;
	next_line = malloc(sizeof(char) * (i + 2));
	if (!next_line)
		return (NULL);
	while (j < i)
	{
		next_line[j] = line[j];
		j++;
	}
	if (line[j] == '\n')
	{
		next_line[j] = line[j];
		j++;
	}
	next_line[j] = '\0';
	return (next_line);
}

char	*get_next_line(int fd)
{
	static char	*line;
	char		*next_line;

	if (fd < 0 || BUFFER_SIZE <= 0)
		return (NULL);
	line = read_line(fd, line);
	if (!line)
		return (NULL);
	next_line = extract(line);
	line = read_next(line);
	return (next_line);
}
